void gui_c::display()
{
	ImGui::SetNextWindowSize(ImVec2(230, 100));
	ImGui::Begin("Simple BO2 Menu", NULL, ImGuiWindowFlags_NoResize | ImGuiWindowFlags_NoScrollbar);

	this->setup_styling();

	ImGui::Checkbox("Aimbot", &settings.features.toggle_aimbot);
	ImGui::Checkbox("Wallhack + Chams", &settings.visuals.toggle_wallhack);

	ImGui::End();
}

